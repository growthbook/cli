#!/usr/bin/env bash
# Stand up a real GrowthBook server for the live CLI e2e suite and bootstrap
# credentials into .env.local, so `go test -tags live ./test/live/` (and
# ./scripts/gb) have a working server + admin key. Idempotent: re-running against
# an already-provisioned instance is a no-op. See test/live/README.md.
#
#   ./scripts/e2e-setup.sh            # up + bootstrap
#   ./scripts/e2e-teardown.sh         # tear down (docker compose down -v)
set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
COMPOSE="$ROOT/test/live/docker-compose.e2e.yml"
ENV_LOCAL="$ROOT/.env.local"

API_URL="${GB_SERVER_URL:-http://localhost:3100/api}"   # what the CLI hits (/api/v1 under here)
APP_URL="${GB_APP_URL:-http://localhost:3100}"          # internal /auth, /user, /keys, /demo-datasource-project
READY_URL="${API_URL%/}/v1/openapi.yaml"                # public, no-auth readiness probe

# Fixed, throwaway bootstrap identity for the local test org.
EMAIL="e2e@growthbook.local"
NAME="E2E Admin"
PASSWORD="e2e-test-password-123"
COMPANY="E2E Test Org"

echo "==> Bringing up the GrowthBook stack (mongo + growthbook)"
docker compose -f "$COMPOSE" up -d

echo "==> Waiting for the API to be ready at $READY_URL"
for i in $(seq 1 90); do
  if curl -sf -o /dev/null "$READY_URL"; then
    echo "    ready"
    break
  fi
  if [ "$i" -eq 90 ]; then
    echo "ERROR: API did not become ready in time. Check: docker compose -f $COMPOSE logs" >&2
    exit 1
  fi
  sleep 2
done

# Idempotency: if .env.local already holds a key that works, we're done.
if [ -f "$ENV_LOCAL" ] && grep -q '^GBCLI_BEARER_AUTH=' "$ENV_LOCAL"; then
  EXISTING_KEY="$(grep '^GBCLI_BEARER_AUTH=' "$ENV_LOCAL" | head -1 | cut -d= -f2-)"
  if curl -sf -o /dev/null -H "Authorization: Bearer $EXISTING_KEY" "${API_URL%/}/v1/projects"; then
    echo "==> .env.local already has a working key — skipping bootstrap."
    exit 0
  fi
fi

echo "==> Registering the first user (creates superadmin + org + default project)"
FIRST_RESP="$(curl -s -X POST "$APP_URL/auth/firsttime" \
  -H "Content-Type: application/json" \
  -d "{\"email\":\"$EMAIL\",\"name\":\"$NAME\",\"password\":\"$PASSWORD\",\"companyname\":\"$COMPANY\"}")"
JWT="$(echo "$FIRST_RESP" | jq -r '.token // empty')"
if [ -z "$JWT" ]; then
  echo "ERROR: /auth/firsttime did not return a token. Response:" >&2
  echo "$FIRST_RESP" >&2
  echo "(If the org is already configured, run ./scripts/e2e-teardown.sh first for a clean slate.)" >&2
  exit 1
fi

# The internal routes resolve the org from an x-organization header, so fetch
# the org the first user belongs to.
ORG="$(curl -s "$APP_URL/user" -H "Authorization: Bearer $JWT" | jq -r '.organizations[0].id // empty')"
if [ -z "$ORG" ]; then
  echo "ERROR: could not determine organization id from /user." >&2
  exit 1
fi

echo "==> Minting an org admin secret key (org $ORG)"
KEY_RESP="$(curl -s -X POST "$APP_URL/keys" \
  -H "Authorization: Bearer $JWT" \
  -H "x-organization: $ORG" \
  -H "Content-Type: application/json" \
  -d '{"type":"admin","description":"cli-e2e"}')"
SECRET="$(echo "$KEY_RESP" | jq -r '.key.key // empty')"
if [ -z "$SECRET" ]; then
  echo "ERROR: /keys did not return a secret key. Response:" >&2
  echo "$KEY_RESP" >&2
  exit 1
fi

# Best-effort: create the built-in demo datasource + sample data so the
# datasource-dependent phases (metrics/dimensions/segments/experiments/fact
# tables) can run. Non-fatal — those tests skip cleanly if it's absent.
echo "==> Creating the demo datasource (best effort)"
if curl -sf -o /dev/null -X POST "$APP_URL/demo-datasource-project" \
  -H "Authorization: Bearer $JWT" -H "x-organization: $ORG" \
  -H "Content-Type: application/json" -d '{}'; then
  echo "    demo datasource created"
else
  echo "    (skipped — datasource-dependent tests will be reported as uncovered)"
fi

echo "==> Writing $ENV_LOCAL"
cat > "$ENV_LOCAL" <<EOF
# Generated by scripts/e2e-setup.sh for the live CLI e2e suite. Gitignored.
GBCLI_BEARER_AUTH=$SECRET
GB_SERVER_URL=$API_URL
EOF

echo "==> Done. Verify with:  ./scripts/gb projects list -o json"
